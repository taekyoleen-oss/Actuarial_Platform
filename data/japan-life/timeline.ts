/** 변천 유형 — 칩·마커 색은 globals.css --chip-* 팔레트만 사용 (teal/amber/rose) */
export type ChangeKind = "rename" | "merge" | "end";

export interface LifeTag {
  label: string;
  kind: ChangeKind;
}

export interface LifeEvent {
  /** 연도 표기 (원문 그대로, 예: "1939–1947") */
  years: string;
  title: string;
  tags: LifeTag[];
  paragraphs: string[];
}

/** 시대 구분 — 타임라인 시각화용 구획 */
export interface LifeEra {
  id: string;
  label: string;
  period: string;
  events: LifeEvent[];
}

/** 회사별 변천 흐름의 한 단계 — kind 없으면 중립(설립 등) */
export interface LifeRouteStep {
  text: string;
  kind?: ChangeKind;
}

export interface LifeCompany {
  name: string;
  intro: string;
  route: LifeRouteStep[];
}

/** 불릿 목록 카드 (파산 배경·계약자 보호·소멸 사례) */
export interface LifeInfoCard {
  title: string;
  items: string[];
}

/** 접이식 보충 설명 */
export interface LifeDetail {
  summary: string;
  body: string;
}

/** 파산 회사 요약표 행 */
export interface LifeBankruptcyRow {
  company: string;
  start: string;
  successor: string;
  note: string;
}

