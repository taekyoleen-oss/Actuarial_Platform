/* ───────────────────────── TOC ───────────────────────── */

export interface TocItem {
  id: string;
  label: string;
  level: 1 | 2 | 3;
  /** 장 표제(섹션이 새 장을 시작할 때) */
  chapter?: string;
  /** 절 그룹 표제(섹션이 새 그룹을 시작할 때) */
  group?: string;
}

/** 마크업(**, ^n^) 제거한 평문 */
export function plainText(text: string): string {
  return text.replace(/\*\*/g, "").replace(/\^([^^]+)\^/g, "$1");
}

export const TRENDS_TOC: TocItem[] = TRENDS_SECTIONS.map((s) => ({
  id: s.id,
  label: plainText(s.tocLabel ?? s.title),
  level: s.level,
  chapter: s.chapter,
  group: s.group ? plainText(s.group) : undefined,
}));

/** 자체 대조용 — 본문 통계 */
export const TRENDS_COUNTS = {
  sections: TRENDS_SECTIONS.length,
  tables: TRENDS_SECTIONS.flatMap((s) => s.blocks).filter(
    (b) => b.kind === "table",
  ).length,
  charts: TRENDS_SECTIONS.flatMap((s) => s.blocks).filter(
    (b) => b.kind === "figure",
  ).length,
};
