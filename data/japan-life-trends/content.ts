/* ───────────────────────── 타입 ───────────────────────── */

export type ValueFmt = "int" | "fixed1" | "cho-eok" | "none";

export interface ChartSeries {
  name: string;
  data: number[];
}

export interface ChartData {
  type: "bar" | "line";
  /** 카테고리(세로 막대: x축, 가로 누적: 행 라벨) */
  labels: string[];
  series: ChartSeries[];
  /** 누적 세로 막대 */
  stacked?: boolean;
  /** 가로 100% 누적(구성비, %) */
  horizontal?: boolean;
  unit?: string;
  unitRight?: string;
  /** 콤보(막대+꺾은선, 우축) 오버레이 */
  line?: { name: string; data: number[]; fmt?: ValueFmt };
  /** 막대 값 라벨 포맷 (기본 int) */
  valueFmt?: ValueFmt;
  /** 누적 합계 라벨 포맷 */
  totalFmt?: ValueFmt;
  /** 누적 합계 라벨 표시 여부 (기본 true) */
  showTotals?: boolean;
  /** 누적 구간 라벨 표시 여부 (기본: 구간 높이에 따라 자동) */
  segLabels?: boolean;
  /** 단일 시리즈 막대 커스텀 라벨(원문 표기 그대로) */
  barLabels?: string[];
  /** 구성 내역형(원본 도넛) — 가로 구성비 막대 + 내역 목록으로 표현 */
  composition?: { label: string; amount: string; percent: number }[];
  /** 구성 내역형 합계 표기 */
  total?: string;
}

export interface TableCell {
  label: string;
  colSpan?: number;
  rowSpan?: number;
}

export interface TableData {
  /** 단위 캡션 — 예: "(만 건, %, 억 엔)" */
  caption?: string;
  headers: TableCell[][];
  rows: string[][];
  /** 굵게 표시할 행 인덱스(합계 행 등) */
  boldRows?: number[];
  /** 모든 셀 좌측 정렬(목록형 표) */
  plain?: boolean;
}

export type Block =
  | { kind: "h"; text: string }
  | { kind: "p"; text: string }
  | { kind: "twocol"; paragraphs: string[] }
  | { kind: "bullet"; text: string }
  | { kind: "ref"; text: string }
  | { kind: "figure"; title?: string; chart: ChartData; notes?: string[] }
  | { kind: "table"; title?: string; table: TableData; notes?: string[] }
  | { kind: "box"; title: string; paragraphs: string[] }
  | { kind: "note"; text: string }
  | { kind: "footnotes"; items: string[] };

export interface TrendsSection {
  id: string;
  /** 장 표제 — 직전 섹션과 다를 때 렌더링 (예: "I. 계약 동향") */
  chapter?: string;
  /** 절 그룹 표제 — 직전 섹션과 다를 때 렌더링 (예: "1. 개인용 상품^1^") */
  group?: string;
  title: string;
  /** TOC 표기(기본 title) */
  tocLabel?: string;
  level: 1 | 2 | 3;
  blocks: Block[];
}

